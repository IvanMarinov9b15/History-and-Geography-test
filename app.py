import streamlit as st

st.title("Тест по География и История")
st.write("Отговорете на въпросите и проверете знанията си.")

score = 0

st.subheader("1. География")
q1 = st.text_input("Коя е най-дългата река в света?", key="q1")

st.subheader("2. История")
q2 = st.selectbox(
    "През коя година е основана българската държава?",
    ["Изберете отговор", "632 г.", "681 г.", "1018 г."],
    key="q2"
)

st.subheader("3. География")
q3 = st.radio(
    "Кой е най-високият връх на Балканския полуостров?",
    ["Вихрен", "Олимп", "Мусала"],
    key="q3"
)

st.subheader("4. История")
q4 = st.text_input("Кой е известен като 'Апостола на свободата'?", key="q4")

st.subheader("5. География")
q5 = st.selectbox(
    "Кой е най-големият океан на Земята?",
    ["Изберете отговор", "Атлантически", "Индийски", "Тихи"],
    key="q5"
)

if st.button("Провери резултата"):
    if q1.strip().lower() in ["нил", "река нил"]:
        score += 1
    
    if q2 == "681 г.":
        score += 1
        
    if q3 == "Мусала":
        score += 1
        
    if q4.strip().lower() in ["васил левски", "левски"]:
        score += 1
        
    if q5 == "Тихи":
        score += 1

    if score == 5:
        st.success(f"Резултат: {score}/5. Всички отговори са верни.")
    elif score >= 3:
        st.info(f"Резултат: {score}/5. Добър опит.")
    else:
        st.error(f"Резултат: {score}/5. Опитайте отново.")
